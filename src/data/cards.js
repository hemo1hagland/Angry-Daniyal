// Henter alle kort for en modus, inkludert egendefinerte fra localStorage.
export function getCardsForMode(modeId, customCards = {}) {
  const base = CARDS[modeId] || [];
  const custom = customCards[modeId] || [];
  return [...base, ...custom];
}

// Trekker et tilfeldig kort for en gitt modus.
export function drawCard(modeId, customCards = {}) {
  const alle = getCardsForMode(modeId, customCards);
  if (alle.length === 0) return "Ingen kort tilgjengelig.";
  return alle[Math.floor(Math.random() * alle.length)];
}
